    public void CandyStore(int[] level) {
        this.level = level;
        
        nextOrder();
    }
    
    public void nextOrder() {
        price = 0;
        payment = 0;
        changeK = null;
        while (price < 1) {
            price = rnd.nextInt(level[level.length - 1]);
        }
        while (price > payment) {
            payment = rnd.nextInt(level[level.length - 1]);
        }
        
        num.setNumberK(price);
        priceK = num.getNumberK();
        num.setNumberK(payment);
        paymentK = num.getNumberK();
        answer = payment - price;
        num.setNumberK(answer);
        answerK = num.getNumberK();
        
        
        priceLabel.setText(priceK);
        paymentLabel.setText(paymentK);
        changeLabel.setText(changeK);
    }
    
    
