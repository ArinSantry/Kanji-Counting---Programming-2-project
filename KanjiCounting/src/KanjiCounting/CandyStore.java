    private void changeLabelActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_changeLabelActionPerformed
        // TODO add your handling code here:
        input = changeLabel.getText();
        if (input.equals(answerK)) {
            JOptionPane.showMessageDialog( null, correct);
            score++;
            nextOrder();
        }
        else {
            JOptionPane.showMessageDialog( null, incorrect);
            mistakes++;
            if (mistakes == 10) {
                JOptionPane.showMessageDialog( null, "Oh no! You've been fired");
                JOptionPane.showMessageDialog( null, "Your score was: " + score);
                mistakes = 0;
                score = 0;
                nextOrder();
            }

        }
    }//GEN-LAST:event_changeLabelActionPerformed

